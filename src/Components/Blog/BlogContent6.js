<p className='mb-3 font'>
The news of multiple farmer suicides in Mandya, Karnataka, in 2015 shook Divya Shetty. Hailing from a family of farmers, it touched a raw nerve as her grandfather, also a farmer, died by suicide when she was a young girl.
<br /> <br />
“My grandfather had throat cancer. My family borrowed a lot of money for his treatment but it was not enough. He then took the decision to end his life. After that, my parents were clear that I should not get into farming. They ensured that I got a good education and wanted me to have a white-collar job. So, I completed my engineering and worked at an IT firm,” says 31-year-old Divya.</p>

